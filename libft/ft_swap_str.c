#include "libft.h"

void	ft_swap_str(char **tab, int i1, int i2)
{
	char	*tmp;

	if (i1 == i2)
		return ;
	tmp = tab[i1];
	tab[i1] = tab[i2];
	tab[i2] = tmp;
}
